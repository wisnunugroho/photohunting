package com.wisnu.photohunting.model;

public class User
{
    String userId;
    String userName;
    String userEmail;
    String userPasword;
    String userPhotoProfile;
    String userStatus;
    String userStatusDate;
    String userSince;

    public String getUserId()
    {
        return userId;
    }

    public void setUserId(String userId)
    {
        this.userId = userId;
    }

    public String getUserName()
    {
        return userName;
    }

    public void setUserName(String userName)
    {
        this.userName = userName;
    }

    public String getUserEmail()
    {
        return userEmail;
    }

    public void setUserEmail(String userEmail)
    {
        this.userEmail = userEmail;
    }

    public String getUserPasword()
    {
        return userPasword;
    }

    public void setUserPasword(String userPasword)
    {
        this.userPasword = userPasword;
    }

    public String getUserPhotoProfile()
    {
        return userPhotoProfile;
    }

    public void setUserPhotoProfile(String userPhotoProfile)
    {
        this.userPhotoProfile = userPhotoProfile;
    }

    public String getUserStatus()
    {
        return userStatus;
    }

    public void setUserStatus(String userStatus)
    {
        this.userStatus = userStatus;
    }

    public String getUserStatusDate()
    {
        return userStatusDate;
    }

    public void setUserStatusDate(String userStatusDate)
    {
        this.userStatusDate = userStatusDate;
    }

    public String getUserSince()
    {
        return userSince;
    }

    public void setUserSince(String userSince)
    {
        this.userSince = userSince;
    }
}
